#include "libft.h"

int		ft_isalnum(int c)
{
	if (!(((c - 'a' >= 0) && (c - 'a' < 26))
		|| ((c - 'A' >= 0) && (c - 'A' < 26))
		|| ((c - '0' >= 0) && (c - '0' < 10))))
		return (0);
	return (1);
}
