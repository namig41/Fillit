#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	ch;

	i = -1;
	ch = c;
	while (s[++i])
	{
		if (s[i] == c)
			return ((char*)&s[i]);
	}
	if (s[i] == c)
		return ((char*)&s[i]);
	return (NULL);
}
